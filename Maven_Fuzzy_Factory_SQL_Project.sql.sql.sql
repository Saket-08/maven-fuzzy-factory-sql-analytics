-- ============================================================
-- TABLE 1: products
-- ============================================================
CREATE TABLE products (
    product_id      INT             GENERATED BY DEFAULT AS IDENTITY,
    created_at      TIMESTAMP       NOT NULL,
    product_name    VARCHAR(100)    NOT NULL,

    CONSTRAINT pk_products PRIMARY KEY (product_id)
);

-- ============================================================
-- TABLE 2: website_sessions
-- ============================================================
CREATE TABLE website_sessions (
    website_session_id  BIGINT          GENERATED BY DEFAULT AS IDENTITY,
    created_at          TIMESTAMP       NOT NULL,
    user_id             BIGINT          NOT NULL,
    is_repeat_session   BOOLEAN         NOT NULL DEFAULT FALSE,
    utm_source          VARCHAR(50),
    utm_campaign        VARCHAR(50),
    utm_content         VARCHAR(50),
    device_type         VARCHAR(20),
    http_referer        VARCHAR(200),

    CONSTRAINT pk_website_sessions PRIMARY KEY (website_session_id)
);

-- ============================================================
-- TABLE 3: website_pageviews
-- ============================================================
CREATE TABLE website_pageviews (
    website_pageview_id BIGINT          GENERATED BY DEFAULT AS IDENTITY,
    created_at          TIMESTAMP       NOT NULL,
    website_session_id  BIGINT          NOT NULL,
    pageview_url        VARCHAR(200)    NOT NULL,

    CONSTRAINT pk_website_pageviews  PRIMARY KEY (website_pageview_id),
    CONSTRAINT fk_pageviews_session  FOREIGN KEY (website_session_id)
        REFERENCES website_sessions (website_session_id)
);

-- ============================================================
-- TABLE 4: orders
-- ============================================================
CREATE TABLE orders (
    order_id            INT             GENERATED BY DEFAULT AS IDENTITY,
    created_at          TIMESTAMP       NOT NULL,
    website_session_id  BIGINT          NOT NULL,
    user_id             BIGINT          NOT NULL,
    primary_product_id  INT             NOT NULL,
    items_purchased     SMALLINT        NOT NULL DEFAULT 1,
    price_usd           DECIMAL(6,2)    NOT NULL,
    cogs_usd            DECIMAL(6,2)    NOT NULL,

    CONSTRAINT pk_orders             PRIMARY KEY (order_id),
    CONSTRAINT fk_orders_session     FOREIGN KEY (website_session_id)
        REFERENCES website_sessions (website_session_id),
    CONSTRAINT fk_orders_product     FOREIGN KEY (primary_product_id)
        REFERENCES products (product_id),

    CONSTRAINT chk_price_positive    CHECK (price_usd > 0),
    CONSTRAINT chk_cogs_positive     CHECK (cogs_usd > 0),
    CONSTRAINT chk_cogs_lt_price     CHECK (cogs_usd < price_usd)
);

-- ============================================================
-- TABLE 5: order_items
-- ============================================================
CREATE TABLE order_items (
    order_item_id   INT             GENERATED BY DEFAULT AS IDENTITY,
    created_at      TIMESTAMP       NOT NULL,
    order_id        INT             NOT NULL,
    product_id      INT             NOT NULL,
    is_primary_item BOOLEAN         NOT NULL DEFAULT FALSE,
    price_usd       DECIMAL(6,2)    NOT NULL,
    cogs_usd        DECIMAL(6,2)    NOT NULL,

    CONSTRAINT pk_order_items        PRIMARY KEY (order_item_id),
    CONSTRAINT fk_items_order        FOREIGN KEY (order_id)
        REFERENCES orders (order_id),
    CONSTRAINT fk_items_product      FOREIGN KEY (product_id)
        REFERENCES products (product_id),

    CONSTRAINT chk_item_price        CHECK (price_usd > 0),
    CONSTRAINT chk_item_cogs         CHECK (cogs_usd > 0)
);

-- ============================================================
-- TABLE 6: order_item_refunds
-- ============================================================
CREATE TABLE order_item_refunds (
    order_item_refund_id    INT             GENERATED BY DEFAULT AS IDENTITY,
    created_at              TIMESTAMP       NOT NULL,
    order_item_id           INT             NOT NULL,
    order_id                INT             NOT NULL,
    refund_amount_usd       DECIMAL(6,2)    NOT NULL,

    CONSTRAINT pk_order_item_refunds PRIMARY KEY (order_item_refund_id),
    CONSTRAINT fk_refunds_order_item FOREIGN KEY (order_item_id)
        REFERENCES order_items (order_item_id),
    CONSTRAINT fk_refunds_order      FOREIGN KEY (order_id)
        REFERENCES orders (order_id),

    CONSTRAINT chk_refund_positive   CHECK (refund_amount_usd > 0)
);


