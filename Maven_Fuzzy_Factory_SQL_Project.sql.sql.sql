-- ============================================================
-- STORED PROCEDURE 2: Get Monthly Revenue Report
-- Business Use: Revenue report for any given year
-- ============================================================
CREATE OR REPLACE PROCEDURE get_monthly_revenue_report(
    IN p_year INT
)
LANGUAGE plpgsql
AS $$
BEGIN
    SELECT
        DATE_TRUNC('month', created_at)         AS order_month,
        COUNT(DISTINCT order_id)                AS total_orders,
        SUM(price_usd)                          AS total_revenue,
        SUM(price_usd - cogs_usd)               AS total_profit,
        ROUND(AVG(price_usd), 2)                AS avg_order_value,
        SUM(items_purchased)                    AS total_items_sold
    FROM orders
    WHERE EXTRACT(YEAR FROM created_at) = p_year
    GROUP BY DATE_TRUNC('month', created_at)
    ORDER BY order_month;
END;
$$;
