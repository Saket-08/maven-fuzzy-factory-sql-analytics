-- ============================================================
-- QUERY 1: Total Revenue, Cost & Profit by Product
-- Business Question: Which products are the most profitable?
-- ============================================================
SELECT
    p.product_name,
    COUNT(DISTINCT o.order_id)              AS total_orders,
    SUM(oi.price_usd)                       AS total_revenue,
    SUM(oi.cogs_usd)                        AS total_cost,
    SUM(oi.price_usd - oi.cogs_usd)         AS total_profit,
    ROUND(
        SUM(oi.price_usd - oi.cogs_usd) /
        SUM(oi.price_usd) * 100, 2
    )                                        AS profit_margin_pct
FROM orders o
JOIN order_items oi ON o.order_id = oi.order_id
JOIN products p    ON oi.product_id = p.product_id
GROUP BY p.product_name
ORDER BY total_profit DESC;


-- ============================================================
-- QUERY 2: Monthly Revenue Trend
-- Business Question: How has revenue grown month over month?
-- ============================================================
SELECT
    DATE_TRUNC('month', o.created_at)       AS order_month,
    COUNT(DISTINCT o.order_id)              AS total_orders,
    SUM(o.price_usd)                        AS total_revenue,
    SUM(o.price_usd - o.cogs_usd)           AS total_profit,
    ROUND(AVG(o.price_usd), 2)              AS avg_order_value
FROM orders o
JOIN order_items oi ON o.order_id = oi.order_id
GROUP BY DATE_TRUNC('month', o.created_at)
ORDER BY order_month;


-- ============================================================
-- QUERY 3: Refund Rate by Product
-- Business Question: Which products have the highest refund rates?
-- ============================================================
SELECT
    p.product_name,
    COUNT(DISTINCT oi.order_item_id)        AS total_items_sold,
    COUNT(DISTINCT r.order_item_refund_id)  AS total_refunds,
    ROUND(
        COUNT(DISTINCT r.order_item_refund_id)::DECIMAL /
        NULLIF(COUNT(DISTINCT oi.order_item_id), 0) * 100, 2
    )                                        AS refund_rate_pct,
    SUM(COALESCE(r.refund_amount_usd, 0))   AS total_refund_amount
FROM order_items oi
JOIN products p         ON oi.product_id = p.product_id
LEFT JOIN order_item_refunds r ON oi.order_item_id = r.order_item_id
GROUP BY p.product_name
ORDER BY refund_rate_pct DESC;


-- ============================================================
-- QUERY 4: Cross-Sell Analysis
-- Business Question: Which products are bought together most often?
-- ============================================================
SELECT
    p1.product_name                         AS primary_product,
    p2.product_name                         AS cross_sell_product,
    COUNT(*)                                AS times_bought_together
FROM order_items oi1
JOIN order_items oi2 ON oi1.order_id = oi2.order_id
                     AND oi1.product_id != oi2.product_id
                     AND oi1.is_primary_item = TRUE
                     AND oi2.is_primary_item = FALSE
JOIN products p1 ON oi1.product_id = p1.product_id
JOIN products p2 ON oi2.product_id = p2.product_id
GROUP BY p1.product_name, p2.product_name
ORDER BY times_bought_together DESC;


-- ============================================================
-- QUERY 5: Top Traffic Sources by Revenue
-- Business Question: Which marketing channels drive the most revenue?
-- ============================================================
SELECT
    COALESCE(ws.utm_source, 'direct')       AS traffic_source,
    COALESCE(ws.utm_campaign, 'none')       AS campaign,
    ws.device_type,
    COUNT(DISTINCT o.order_id)              AS total_orders,
    SUM(o.price_usd)                        AS total_revenue,
    ROUND(AVG(o.price_usd), 2)              AS avg_order_value
FROM orders o
JOIN website_sessions ws ON o.website_session_id = ws.website_session_id
GROUP BY
    COALESCE(ws.utm_source, 'direct'),
    COALESCE(ws.utm_campaign, 'none'),
    ws.device_type
ORDER BY total_revenue DESC;


-- ============================================================
-- QUERY 6: Conversion Rate by Traffic Source
-- Business Question: Which traffic source converts best?
-- ============================================================
SELECT
    COALESCE(ws.utm_source, 'direct')       AS traffic_source,
    COUNT(DISTINCT ws.website_session_id)   AS total_sessions,
    COUNT(DISTINCT o.order_id)              AS total_orders,
    ROUND(
        COUNT(DISTINCT o.order_id)::DECIMAL /
        NULLIF(COUNT(DISTINCT ws.website_session_id), 0) * 100, 2
    )                                        AS conversion_rate_pct
FROM website_sessions ws
LEFT JOIN orders o ON ws.website_session_id = o.website_session_id
GROUP BY COALESCE(ws.utm_source, 'direct')
ORDER BY conversion_rate_pct DESC;


-- ============================================================
-- QUERY 7: Repeat vs New Customer Revenue
-- Business Question: How much revenue comes from repeat customers?
-- ============================================================
SELECT
    CASE
        WHEN ws.is_repeat_session = TRUE THEN 'Repeat Customer'
        ELSE 'New Customer'
    END                                     AS customer_type,
    COUNT(DISTINCT o.order_id)              AS total_orders,
    SUM(o.price_usd)                        AS total_revenue,
    ROUND(AVG(o.price_usd), 2)              AS avg_order_value,
    ROUND(
        COUNT(DISTINCT o.order_id)::DECIMAL /
        SUM(COUNT(DISTINCT o.order_id)) OVER() * 100, 2
    )                                        AS order_share_pct
FROM orders o
JOIN website_sessions ws ON o.website_session_id = ws.website_session_id
GROUP BY ws.is_repeat_session
ORDER BY total_revenue DESC;


-- ============================================================
-- QUERY 8: Clean vs Refunded Orders
-- Business Question: What percentage of orders were refunded?
-- ============================================================
SELECT
    order_status,
    COUNT(*)                                AS total_orders,
    SUM(total_revenue)                      AS total_revenue,
    ROUND(
        COUNT(*)::DECIMAL /
        SUM(COUNT(*)) OVER() * 100, 2
    )                                        AS pct_of_orders
FROM (
    SELECT
        o.order_id,
        o.price_usd                         AS total_revenue,
        CASE
            WHEN r.order_id IS NOT NULL THEN 'Refunded'
            ELSE 'Clean'
        END                                 AS order_status
    FROM orders o
    LEFT JOIN order_item_refunds r ON o.order_id = r.order_id
) AS order_summary
GROUP BY order_status
ORDER BY total_orders DESC;


