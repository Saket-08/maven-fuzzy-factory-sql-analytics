-- TRIGGER SETUP: Refund Audit Log Table
-- ============================================================
CREATE TABLE IF NOT EXISTS refund_audit_log (
    log_id                  SERIAL          PRIMARY KEY,
    order_item_refund_id    INT             NOT NULL,
    order_id                INT             NOT NULL,
    refund_amount_usd       DECIMAL(6,2)    NOT NULL,
    action_type             VARCHAR(10)     NOT NULL,
    action_timestamp        TIMESTAMP       NOT NULL DEFAULT NOW(),
    performed_by            VARCHAR(50)     DEFAULT CURRENT_USER
);


-- ============================================================
-- TRIGGER 1: Log Every New Refund Automatically
-- ============================================================
CREATE OR REPLACE FUNCTION log_new_refund()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    INSERT INTO refund_audit_log (
        order_item_refund_id, order_id, refund_amount_usd,
        action_type, action_timestamp, performed_by
    ) VALUES (
        NEW.order_item_refund_id, NEW.order_id, NEW.refund_amount_usd,
        'INSERT', NOW(), CURRENT_USER
    );
    RETURN NEW;
END;
$$;

CREATE OR REPLACE TRIGGER trg_log_new_refund
    AFTER INSERT ON order_item_refunds
    FOR EACH ROW EXECUTE FUNCTION log_new_refund();


-- ============================================================
-- TRIGGER 2: Prevent Negative Refund Amounts
-- ============================================================
CREATE OR REPLACE FUNCTION validate_refund_amount()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    IF NEW.refund_amount_usd <= 0 THEN
        RAISE EXCEPTION
            'Invalid refund amount: %. Refund must be greater than zero.',
            NEW.refund_amount_usd;
    END IF;
    RETURN NEW;
END;
$$;

CREATE OR REPLACE TRIGGER trg_validate_refund_amount
    BEFORE INSERT ON order_item_refunds
    FOR EACH ROW EXECUTE FUNCTION validate_refund_amount();


-- ============================================================
-- TRIGGER 3: Prevent Refund Exceeding Original Item Price
-- ============================================================
CREATE OR REPLACE FUNCTION validate_refund_vs_price()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
DECLARE
    v_original_price DECIMAL(6,2);
BEGIN
    SELECT price_usd INTO v_original_price
    FROM order_items
    WHERE order_item_id = NEW.order_item_id;

    IF NEW.refund_amount_usd > v_original_price THEN
        RAISE EXCEPTION
            'Refund amount % exceeds original item price of %.',
            NEW.refund_amount_usd, v_original_price;
    END IF;
    RETURN NEW;
END;
$$;

CREATE OR REPLACE TRIGGER trg_validate_refund_vs_price
    BEFORE INSERT ON order_item_refunds
    FOR EACH ROW EXECUTE FUNCTION validate_refund_vs_price();


