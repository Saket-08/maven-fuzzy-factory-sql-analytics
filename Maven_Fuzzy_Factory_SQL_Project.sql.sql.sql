-- ============================================================
-- STORED PROCEDURE 1: Get Product Sales Summary
-- Business Use: Pull sales summary for any product by name
-- ============================================================
CREATE OR REPLACE PROCEDURE get_product_sales_summary(
    IN p_product_name VARCHAR(100)
)
LANGUAGE plpgsql
AS $$
BEGIN
    SELECT
        p.product_name,
        COUNT(DISTINCT o.order_id)              AS total_orders,
        SUM(oi.price_usd)                       AS total_revenue,
        SUM(oi.cogs_usd)                        AS total_cost,
        SUM(oi.price_usd - oi.cogs_usd)         AS total_profit,
        ROUND(AVG(oi.price_usd), 2)             AS avg_selling_price,
        ROUND(
            SUM(oi.price_usd - oi.cogs_usd) /
            SUM(oi.price_usd) * 100, 2
        )                                        AS profit_margin_pct
    FROM products p
    JOIN order_items oi  ON p.product_id = oi.product_id
    JOIN orders o        ON oi.order_id = o.order_id
    WHERE LOWER(p.product_name) = LOWER(p_product_name);
END;
$$;
