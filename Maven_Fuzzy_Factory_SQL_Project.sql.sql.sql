-- ============================================================
-- QUERY 1: Running Total Revenue Over Time
-- Business Question: What is our cumulative revenue growth?
-- ============================================================
SELECT
    DATE_TRUNC('month', o.created_at)           AS order_month,
    SUM(o.price_usd)                            AS monthly_revenue,
    SUM(SUM(o.price_usd))
        OVER (ORDER BY DATE_TRUNC('month', o.created_at))
                                                AS running_total_revenue
FROM orders o
GROUP BY DATE_TRUNC('month', o.created_at)
ORDER BY order_month;


-- ============================================================
-- QUERY 2: Month Over Month Revenue Growth
-- Business Question: Is revenue growing or declining each month?
-- ============================================================
WITH monthly_revenue AS (
    SELECT
        DATE_TRUNC('month', created_at)         AS order_month,
        SUM(price_usd)                          AS total_revenue
    FROM orders
    GROUP BY DATE_TRUNC('month', created_at)
)
SELECT
    order_month,
    total_revenue,
    LAG(total_revenue) OVER
        (ORDER BY order_month)                  AS prev_month_revenue,
    ROUND(
        (total_revenue - LAG(total_revenue)
            OVER (ORDER BY order_month)) /
        LAG(total_revenue)
            OVER (ORDER BY order_month) * 100
    , 2)                                        AS mom_growth_pct
FROM monthly_revenue
ORDER BY order_month;


-- ============================================================
-- QUERY 3: Product Revenue Ranking Per Year
-- Business Question: How does each product rank by revenue?
-- ============================================================
SELECT
    p.product_name,
    DATE_TRUNC('year', oi.created_at)           AS order_year,
    SUM(oi.price_usd)                           AS total_revenue,
    RANK() OVER (
        PARTITION BY DATE_TRUNC('year', oi.created_at)
        ORDER BY SUM(oi.price_usd) DESC
    )                                           AS revenue_rank
FROM order_items oi
JOIN products p ON oi.product_id = p.product_id
GROUP BY p.product_name, DATE_TRUNC('year', oi.created_at)
ORDER BY order_year, revenue_rank;


-- ============================================================
-- QUERY 4: Customer Order Frequency Analysis
-- Business Question: How many orders does each customer place?
-- ============================================================
WITH customer_orders AS (
    SELECT
        user_id,
        order_id,
        created_at,
        price_usd,
        COUNT(order_id) OVER
            (PARTITION BY user_id)              AS total_orders_by_customer,
        SUM(price_usd) OVER
            (PARTITION BY user_id)              AS total_spent_by_customer,
        RANK() OVER (
            PARTITION BY user_id
            ORDER BY created_at
        )                                       AS order_sequence
    FROM orders
)
SELECT
    total_orders_by_customer                    AS orders_placed,
    COUNT(DISTINCT user_id)                     AS number_of_customers,
    ROUND(AVG(total_spent_by_customer), 2)      AS avg_total_spent
FROM customer_orders
WHERE order_sequence = 1
GROUP BY total_orders_by_customer
ORDER BY total_orders_by_customer DESC;


-- ============================================================
-- QUERY 5: Top 3 Revenue Days Per Month
-- Business Question: What are the peak sales days each month?
-- ============================================================
WITH daily_revenue AS (
    SELECT
        DATE_TRUNC('month', created_at)         AS order_month,
        DATE_TRUNC('day', created_at)           AS order_day,
        SUM(price_usd)                          AS daily_revenue
    FROM orders
    GROUP BY
        DATE_TRUNC('month', created_at),
        DATE_TRUNC('day', created_at)
),
ranked_days AS (
    SELECT
        order_month,
        order_day,
        daily_revenue,
        RANK() OVER (
            PARTITION BY order_month
            ORDER BY daily_revenue DESC
        )                                       AS day_rank
    FROM daily_revenue
)
SELECT
    order_month,
    order_day,
    daily_revenue,
    day_rank
FROM ranked_days
WHERE day_rank <= 3
ORDER BY order_month, day_rank;


-- ============================================================
-- QUERY 6: Product Performance vs Average
-- Business Question: Which products perform above average?
-- ============================================================
WITH product_revenue AS (
    SELECT
        p.product_name,
        SUM(oi.price_usd)                       AS total_revenue,
        AVG(SUM(oi.price_usd)) OVER ()          AS avg_revenue_all_products
    FROM order_items oi
    JOIN products p ON oi.product_id = p.product_id
    GROUP BY p.product_name
)
SELECT
    product_name,
    total_revenue,
    ROUND(avg_revenue_all_products, 2)          AS avg_revenue,
    ROUND(total_revenue - avg_revenue_all_products, 2)
                                                AS variance_from_avg,
    CASE
        WHEN total_revenue > avg_revenue_all_products THEN 'Above Average'
        WHEN total_revenue = avg_revenue_all_products THEN 'At Average'
        ELSE 'Below Average'
    END                                         AS performance_flag
FROM product_revenue
ORDER BY total_revenue DESC;


-- ============================================================
-- QUERY 7: Refund Rate Trend Over Time
-- Business Question: Is our refund rate improving over time?
-- ============================================================
WITH monthly_refunds AS (
    SELECT
        DATE_TRUNC('month', o.created_at)       AS order_month,
        COUNT(DISTINCT o.order_id)              AS total_orders,
        COUNT(DISTINCT r.order_item_refund_id)  AS total_refunds,
        ROUND(
            COUNT(DISTINCT r.order_item_refund_id)::DECIMAL /
            COUNT(DISTINCT o.order_id) * 100, 2
        )                                       AS refund_rate_pct
    FROM orders o
    LEFT JOIN order_item_refunds r ON o.order_id = r.order_id
    GROUP BY DATE_TRUNC('month', o.created_at)
)
SELECT
    order_month,
    total_orders,
    total_refunds,
    refund_rate_pct,
    LAG(refund_rate_pct) OVER
        (ORDER BY order_month)                  AS prev_month_refund_rate,
    ROUND(
        refund_rate_pct - LAG(refund_rate_pct)
            OVER (ORDER BY order_month)
    , 2)                                        AS refund_rate_change
FROM monthly_refunds
ORDER BY order_month;


-- ============================================================
-- QUERY 8: Customer Segmentation by Spending
-- Business Question: Who are our high, medium, low value customers?
-- ============================================================
WITH customer_spending AS (
    SELECT
        user_id,
        COUNT(order_id)                         AS total_orders,
        SUM(price_usd)                          AS total_spent,
        NTILE(3) OVER (
            ORDER BY SUM(price_usd) DESC
        )                                       AS spending_tier
    FROM orders
    GROUP BY user_id
)
SELECT
    CASE spending_tier
        WHEN 1 THEN 'High Value'
        WHEN 2 THEN 'Medium Value'
        WHEN 3 THEN 'Low Value'
    END                                         AS customer_segment,
    COUNT(user_id)                              AS total_customers,
    ROUND(AVG(total_orders), 2)                 AS avg_orders,
    ROUND(AVG(total_spent), 2)                  AS avg_spent,
    SUM(total_spent)                            AS segment_revenue
FROM customer_spending
GROUP BY spending_tier
ORDER BY spending_tier;


